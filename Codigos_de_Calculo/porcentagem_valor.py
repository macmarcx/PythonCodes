#Porcentagem de valores

#Input do primeiro valor
valor1 = float(input('Digite o primeiro valor: '))

#Input do segundo valor
valor2 = float(input('Digite o segundo valor: '))

#Divisăo do primeiro valor por 100
div = valor1 / 100

#Multiplicaçăo da divisăo pelo segundo valor
multi = div * valor2

#Porcentagem dos valores e apresentaçăo na tela
print('A Porcentagem de ', valor1, ' sobre ', valor2, ' é ', multi)
